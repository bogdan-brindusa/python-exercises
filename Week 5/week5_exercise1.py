'''
	Which of the following conditions are true, if a = 13 and b = 14 ? 
 
		a) a + 1 <= b
		b) a + 1 >= b
		c) a + 1 != b
'''

# variables initialisation:
a = 13
b = 14

if a + 1 <= b:		# first condition
	print('true')
else:
	print('false')

if a + 1 >= b:		# second condition
	print('true')
else:
	print('false')

if a + 1 != b:		# third condition
	print('true')
else:
	print('false')
