'''
	Write code to calculate the average of:
	{3, 11, 78, 112, 4, 18} in one single
	line of code.
'''

# variable 'average' holds the average of
# these six numbers
average = (3 + 11 + 78 + 112 + 4 + 18) / 6

# print on screen the result
print(average)

# calculate the average and print the result
# with two decimals in one line
print("%.2f" %((3 + 11 + 78 + 112 + 4 + 18) / 6))